import pandas as pd
import numpy as np

# === Создаём простой Series (один столбец) ===
sales = pd.Series([1200, 1500, 980, 2100], 
                  index=['Пн', 'Вт', 'Ср', 'Чт'],
                  name='Выручка')

print("Series (выручка по дням):")
print(sales)
print()

# === Создаём DataFrame (таблица) ===
data = {
    'день': ['Пн', 'Вт', 'Ср', 'Чт'],
    'выручка': [1200, 1500, 980, 2100],
    'расходы': [800, 950, 720, 1100]
}

df = pd.DataFrame(data)

print("DataFrame (таблица продаж и расходов):")
print(df)
# === Добавляем столбец "прибыль" ===
df['прибыль'] = df['выручка'] - df['расходы']

print("\nDataFrame с прибылью:")
print(df)

# === Простые бизнес-метрики ===
print("\n=== Бизнес-метрики ===")
print("Общая выручка:", df['выручка'].sum())
print("Общая прибыль:", df['прибыль'].sum())
print("Средняя прибыль в день:", round(df['прибыль'].mean(), 2))